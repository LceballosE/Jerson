          <p> Son mazos con esbirros variados, de bajo coste y con muy pocos o ningún hechizo. Suelen ser muy agresivos e intentan incluir esbirros con Grito de batalla que aporte beneficio, si es posible (Sargento abusón, Clériga del Sol Devastado, Enano Hierro Negro...) o que lo hagan de forma pasiva (Lobo temible alfa, Líder de banda) o que ganen beneficios con el tiempo (Hobgoblin, Hiena carroñera, Aventurero en misión...). Por descontado, los esbirros con cargar suelen ser bienvenidos.
