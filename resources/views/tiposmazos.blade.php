      <div class="sectiontitle">
        <h6 class="heading font-x3">Tempo</h6>
      </div>
      <div class="group"  >
      <article>
        <p>Estos mazos juegan cartas que hacen variar el ritmo de la partida buscando darte ventaja, bien obligando al contrario a hacer malos intercambios, o a gestionar de forma ineficiente su maná.
Un ejemplo podrías tenerlo en el Enterrador: Un esbirro de coste 1 que te obliga a jugar moneda y una carta mejor para quitarlo de en medio, ha modificado tu tempo; El pícaro, con los Porrazos o la Traición son otro ejemplo, al hacerte perder turno al no poder sacarle partido al/a los esbirro(s) que te suben a la mano. Mazos que "bufan" (benefician) a los esbirros o consiguen eliminarte varios tuyos con pocas cartas, también se incluyen aquí, pues para reponerte de su efecto o paliarlo (matando a los esbirros "bufados") deberás variar tu ritmo y, generalmente, se buscará que lo hagas de forma ineficiente.</p>
      </article>
      </div>    
    </section>
  </div>
</div>



<div class="wrapper bgded" style="background-image:url('images/demo/chica.png');">
  <div class="hoc split clear">
    <section>
      <div class="sectiontitle">
      <h6 class="heading font-x3">Ramp</h6>
      </div>
      <div class="group">
        <article>
          <p>Este tipo de mazos buscan jugar cartas antes de su turno, variando normalmente el maná. El ejemplo básico es el druida, que con Estimular o Crecimiento salvaje consiguen ganar cristales de maná y jugar cartas más poderosas antes de tiempo.
Si te fijas en sus curvas de maná, buscan tener pocas cartas de coste bajo, pues el objetivo no será jugarlas, de ahí que parezca una rampa o escalón, teniendo pocas cartas de coste bajo y muchas de coste alto.</p>
        </article>
      </div>
    </section>
  </div>
</div>







