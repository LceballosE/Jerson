        <h6 class="heading font-x3">Tokens</h6>
      </div>
      <div class="group"  >
      <article>
        <p>Este tipo de mazos se basa en llenar la mesa de esbirros de bajo coste o gratuitos (tokens): la Maestra de diablillos, Profesora violeta, la Explosión de diablillos, Preparación para la batalla... Para después "bufarlos" (beneficiarlos) y hacer más daño (piensa en el combo del druida, con Fuerza de la Naturaleza y Rugido salvaje o Poder de lo Salvaje.
Son habituales los Druidas (Profesora violeta más Poder de lo Salvaje, el combo que comenté, Cenarius para beneficiar a los que tenga ya en mesa...) o los Paladines (sobre todo ahora con Preparación, pero también antes con Profesora, Maestra de diablillos y Espada de Justicia)</p>
      </article>   
      </div>    
    </section>
