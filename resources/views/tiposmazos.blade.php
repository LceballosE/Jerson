      <div class="sectiontitle">
      <h6 class="heading font-x3">Agro</h6>
      </div>
      <div class="group">
        <article>
          <p>Un mazo agresivo está orientado a hacer daño y, normalmente, hacerlo rápido. Este tipo de mazos presenta una curva de maná con más cartas de valor bajo que alto. 
El daño puedes hacerlo al adversario directamente (lo de "ir a la cara") o gestionar tus recursos para eliminar amenazas (aunque no es lo habitual; Para ello se lleva algún silencio o algún "removal" que elimine la amenaza).
Fíjate que por amenaza, en este tipo de mazos, nos referimos a algún provocar que evite nuestro daño.</p>
        </article>
      </div>
    </section>
  </div>
</div>


<div class="wrapper bgded" style="background-image:url('images/demo/arco.jpg');">
  <div class="hoc splitl clear">

    <section>
      <div class="sectiontitle">
        <h6 class="heading font-x3">Control</h6>
      </div>
      
        <article>       
        <p>Los mazos control poseen recursos que permitan eso mismo: mantener el control de la partida. Por control nos referimos a la ventaja de cartas, presencia en mesa... Ese tipo de cosas.
Si echamos mano del típico mazo de guerrero, verás que lleva hechizos que permiten eliminar esbirros de forma rápida (Camorras, Ejecutar, Embates sobre esbirros con poca vida, capataces...) a la vez que le proveen de armadura para ir aguantando la partida, armas para atacar...
En otros mazos se emplean cartas con provocar para mantener el control de mesa, y esbirros orientados para mantener presencia.
</p>
</article>
         </section>
    
    </div> 
  </div>




<div class="wrapper bgded" style="background-image:url('images/demo/raw.jpg');">
  <div class="hoc split clear">
    <section>
      <div class="sectiontitle">
      <h6 class="heading font-x3">Midrange</h6>
      </div>
      <div class="group">
        <article>
          <p>Estaría en un término medio. Tus cartas te permiten hacer daño a la vez que mantienes cierto control sobre la partida. Ejemplos de ello pueden ser el cazador o el druida, con mazos en los que llevan recursos bien para limpiarte una mesa, hacerte bastante daño en pocos turnos o poner más esbirros en mesa, ganando presencia.

Normalmente, los mazos Agro buscan acabar la partida en pocos turnos (si se alarga, pierden fuelle y no tendrán recursos para mantener o remontar la partida); Los mazos Control buscan alargar las partidas, para disponer de esbirros fuertes y recursos suficientes para terminar lo poco que le quede al adversario; Los mazos Midrange están en el medio: si es posible, controlan mesa para poder terminar pronto; Si no, tienen recursos para aguantar y remontar pronto la partida.

Los siguientes conceptos, más que a mazos en sí, se refieren a la gestión que hagas de tus recursos para influir en la partida.</p>
        </article>
      </div>
    </section>
  </div>
</div>





<div class="wrapper bgded" style="background-image:url('images/demo/forest.jpg');">
